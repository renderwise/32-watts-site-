                    <h3>1. What are Clear Aligners?</h3>



                    <p>Clear braces or aligners are the custom-made plastic trays that are worn on the teeth for teeth straightening.
                        Unlike braces, there are no metallic wires, brackets or tiny rubber bands.</p>



                    <h3>2. What are the benefits of Clear Aligners?</h3>



                    <p>Aligners have multiple features. They are removable, comfortable, and virtually invisible and allow you
                        to enjoy every aspect of life which makes them preferable over braces.</p>



                    <h3>3. How long should you wear aligners?</h3>



                    <p>Aligners must be worn for 22 hours a day to get the expected result. Aligners should only be removed
                        for eating, drinking, and oral hygiene.</p>



                    <h3>4. What teeth misalignments can be corrected with clear aligners?</h3>



                    <p>Clear aligners help in teeth straightening. Anyone suffering from mal-aligned teeth, overbite, cross-bite,
                        gaps in teeth can take aligner treatment after consulting an orthodontist.</p>



                    <h3>5. How should you clean your aligners?</h3>



                    <p>Use the cleaning kit. If you do not have the cleaning kit, you may clean them by using a soft-bristled
                        toothbrush and recommended toothpaste. Gently brush the aligners and wash them thoroughly with clean
                        water.



                    </p>



                    <h3>6. At what age can you start wearing your clear aligner?</h3>



                    <p>Clear Aligners are suitable for all ages. However, the patient must have all permanent teeth erupted.</p>



                    <br/>



                    <span id="dots"></span>



                    <span id="more">



                        <h2>Clear Aligners vs. Braces</h2>



                        <h3>7. What is the difference between clear aligners and metallic braces?</h3>



                        <p>Aligners are medical grade clear plastic trays that can be worn on the teeth. They are comfortable,
                            flexible, nearly invisible and removable. Whereas braces are metal or ceramic brackets that are
                            bonded/fixed on your teeth till your teeth are aligned. They are easy to notice, hard to clean
                            and non-removable.</p>



                        <h3>8. Do I need to visit the dentist’s clinic often like in case of braces?</h3>



                        <p>Not at all, aligners require only a few dental visits, just to check the treatment progression or
                            any issue with the fitting of aligners. While in braces, a patient needs to visit the dental
                            clinic frequently to tighten the wires and undergo modifications.</p>



                        <h3>9. How is oral hygiene easy with aligners?</h3>



                        <p>When it comes to oral hygiene, it is an essential part of our day to day living and an integral part
                            of our overall health. With braces, you need to maintain oral hygiene many times in a day because
                            of food particles getting stuck in your braces whereas in aligners you just have to wash them
                            whenever you remove them to eat or brush your teeth.</p>



                        <h3>10. Is there any chance of aligner breakage or damage?</h3>



                        <p>Clear aligners are made up of a strong medical grade plastic. There are little or no chances of any
                            breakage or damage if you remove and wear them carefully according to your dentist’s instructions.
                            With braces, the patient faces a tough time in maintaining metallic wires and brackets.</p>



                        <h3>11. What are the food restrictions while wearing clear aligners?</h3>



                        <p>Clear aligners are completely removable. Therefore, you can have any food or drink you want just
                            by removing them. There are no food restrictions in clear aligners. But always clean your mouth
                            and the aligners when you wear them back again.</p>



                        <h2>Do’s and Don’ts of Clear Aligner</h2>



                        <h3>12. Do I Need to Remove Aligners While Eating?</h3>



                        <p>Aligners allow you to eat and drink whatever you want. As aligners are made of medical grade plastic,
                            they are prone to get damaged if exposed to heat or chemicals. Therefore, If you eat or drink
                            anything while wearing aligners, it will diminish the quality and colour of the aligner and may
                            disfigure them. As a result, they will not work efficiently anymore. Therefore, you must remove
                            them during eating or drinking.</p>



                        <h3>13. I Start My Day with Tea/Coffee, Do I Need to Remove Aligners?</h3>



                        <p>Yes, tea and coffee and contains high amounts of colour pigment. If you wear aligners when you are
                            drinking coffee or tea, it will leave colour on the aligner and aligner will become discoloured.
                            So, remove the aligner during such consumptions.</p>



                        <h3>14. Can I Chew Gum While Wearing Aligners?</h3>



                        <p>Chewing gum is not suitable for aligners as it may get stuck in the aligners and damage them. Therefore,
                            orthodontists advise you not to do so.</p>



                        <h3>15. Can I Drink Alcohol When Wearing My Aligners?</h3>



                        <p>No, because of the high sugar content in alcohol, it will discolour the aligners. You should make
                            sure sugar does not get trapped between your teeth and aligner. The same applies to other sugary
                            beverages also.



                        </p>



                        <h3>16. Can I sleep wearing Clear Aligners?</h3>



                        <p>Of course, you can. In fact, while you are sleeping aligners get enough time to work. But clean the
                            aligners and your mouth when you wake up in the morning and insert them back again.</p>



                        <h3>17. Will aligners affect my speech?</h3>



                        <p>Only at the initial stage, you may experience some discomfort/lisp while speaking. Once your mouth
                            becomes habitual to aligners, it won’t affect your speech anymore.</p>



                        <h2>Cases treatable with Aligner</h2>



                        <h3>Can my crooked teeth be treated with clear aligners?</h3>



                        <p>Aligner can effectively straighten crooked teeth. Discuss your requirements with your dentist during
                            the consultation.



                        </p>



                        <h3>What is a poor bite? How can it be corrected?</h3>



                        <p>A poor bite is quite common. Normally your upper teeth overlap the lower teeth by 2 mm. When the
                            lower teeth overlap the upper teeth, it is called a crossbite even if it is a single tooth overlap.
                            Such kind of poor bite can be corrected with the clear aligner treatment.</p>



                        <h3>What if someone is having gaps between teeth?</h3>



                        <p>Aligners can also correct the gaps between teeth. Clear aligners gradually close the gap between
                            the teeth by moving them closer.</p>



